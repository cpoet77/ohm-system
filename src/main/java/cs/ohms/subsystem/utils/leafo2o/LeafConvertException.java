package cs.ohms.subsystem.utils.leafo2o;

/**
 * LeafConvertException
 *
 * @author <a href="https://www.nsleaf.cn">nsleaf</a>
 */
public class LeafConvertException extends Exception{
    public LeafConvertException() {
    }

    public LeafConvertException(String message) {
        super(message);
    }

    public LeafConvertException(String message, Throwable cause) {
        super(message, cause);
    }

    public LeafConvertException(Throwable cause) {
        super(cause);
    }

    public LeafConvertException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
